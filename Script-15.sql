CREATE  TABLE FINANCEIRO(
FINAN_ID INTEGER NOT NULL,
FINAN_NUM INTEGER NOT NULL,
FINAN_EMISSAO VARCHAR(10),
FINAN_VENC VARCHAR(10),
FINAN_PAG VARCHAR(10),
FINAN_VAL NUMBER (8,2),
FINAN_JUROS NUMBER (8,2),
FINAN_MULTA NUMBER (8,2),
FINAN_DESCONTO NUMBER (8,2),
FINAN_TOTAL NUMBER (8,2)
);

CREATE TABLE PAGAR(
FINAN_ID INTEGER NOT NULL,
FOR_ID INTEGER NOT NULL,
BOLETO VARCHAR(50)
);

CREATE TABLE FORNECEDOR(
FOR_ID INTEGER NOT NULL,
FOR_NOME VARCHAR(50),
FOR_RAZAO VARCHAR(50),
FOR_CNPJ VARCHAR(50),
FOR_EMAIL VARCHAR (50),
FOR_DDD NUMBER (2),
FOR_NUMERO NUMBER (9)
);

CREATE TABLE ENDERECO_FORNECEDOR(
ENDERECO_ID INTEGER NOT NULL,
FOR_ID INTEGER NOT NULL
);

CREATE TABLE ENDERECO(
ENDERECO_ID INTEGER NOT NULL,
ENDERECO_LOGRADOURO VARCHAR(100),
ENDERECO_NUMERO NUMBER,
ENDERECO_CEP VARCHAR(8),
ENDEROCO_CIDADE VARCHAR(30),
ENDERECO_ESTADO VARCHAR(30)
);

CREATE TABLE ENDERECO_CLIENTE(
ENDERECO_ID INTEGER NOT NULL,
CLI_ID INTEGER NOT NULL
);


CREATE TABLE RECEBER(
FINAN_ID INTEGER NOT NULL,
CLI_ID INTEGER NOT NULL,
NOTA_FISCAL VARCHAR(10)
);

CREATE TABLE CLIENTE(
CLI_ID INTEGER NOT NULL,
CLI_NOME VARCHAR(50),
CLI_CPF VARCHAR(10),
CLI_EMAIL VARCHAR(50),
CLI_DDD NUMBER(2),
CLI_TELEFONE NUMBER (9)
);

CREATE TABLE USUARIO(
USU_ID INTEGER NOT NULL,
USU_NOME VARCHAR(50),
USU_LOGIN VARCHAR(50),
USU_SENHA VARCHAR(14)
);

