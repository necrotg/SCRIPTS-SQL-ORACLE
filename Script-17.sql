SELECT F2.FINAN_ID, FINAN_VENC, FINAN_PAG, FOR_NOME, FOR_DDD, F.FOR_NUMERO FROM FORNECEDOR f, PAGAR p, FINANCEIRO f2 WHERE F2.FINAN_ID = P.FINAN_ID AND P.FOR_ID = F.FOR_ID AND FOR_NOME LIKE '%INFORMATICA%' AND F2.FINAN_MULTA != 0;  
--5.C
SELECT  FINAN_PAG, SUM(FINAN_TOTAL) FROM CLIENTE c, RECEBER r, FINANCEIRO f WHERE C.CLI_ID =  R.CLI_ID AND R.FINAN_ID = F.FINAN_ID GROUP BY F.FINAN_ID ORDER BY F.FINAN_PAG;  
--5.D
SELECT FOR_NOME, MIN(FINAN_TOTAL), MAX(FINAN_TOTAL) , AVG(FINAN_TOTAL) , FOR_DDD, F.FOR_NUMERO FROM FORNECEDOR f, PAGAR p, FINANCEIRO f2 WHERE F2.FINAN_ID = P.FINAN_ID AND P.FOR_ID = F.FOR_ID GROUP BY F2.FINAN_ID ORDER BY F.FOR_NOME; 
--5.E
SELECT CLI_NOME, AVG(F.FINAN_TOTAL), SUM(F.FINAN_TOTAL) FROM FINANCEIRO f, CLIENTE c, RECEBER r, ENDERECO_CLIENTE ec, ENDERECO e WHERE C.CLI_ID =  R.CLI_ID AND R.FINAN_ID = F.FINAN_ID AND C.CLI_ID = EC.CLI_ID  AND EC.ENDERECO_ID = E.ENDERECO_ID AND E.ENDERECO_ESTADO = 'SP' AND FINAN_DESCONTO != 0 GROUP BY F.FINAN_ID;